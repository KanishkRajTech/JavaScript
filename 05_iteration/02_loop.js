//  While Loop

let index = 0
while ( index <= 10) {
    console.log(`value of index is ${index}`);
    index = index + 2
}



let myArray = ['flash', 'batman', 'superman']

let arr = 0;
while (arr < myArray.length){
    console.log(`value is ${myArray[arr]}`);
    arr ++;
}

// Do While loop


let score = 1

do {
    console.log(`score is ${score}`); // score is 1 to 10
    score++
} while (score <= 10 );

let point = 11 

do {
    console.log(`point is ${point}`); // point is 11
    point++
} while (point <= 10 );